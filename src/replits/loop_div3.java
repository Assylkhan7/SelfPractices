package replits;

public class loop_div3 {
    public static void main(String[] args) {

        for(int a =3; a<=20; a+=3){
            System.out.print(a+" ");
        }

        int i = 3;
        while (i<=20){
            {
                System.out.print(i+" ");
                  i+=3;
              }
        }















    }
}
